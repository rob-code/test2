    <p class="lead text-body-secondary item-padding-top project-description">My reasoning behind this piece perhaps needs some explanation as I was led to it by several strands of thought.</p>

    <p class="lead text-body-secondary project-description">David is of course one of the most recognisable and iconic Renaissance sculptures. Created by Michelangelo in Florence between 1501 and 1504, David stands 17ft tall and is the first colossal sculpture of the High Renaissance. It embodies high art, unrivaled excellence in figurative sculpture, and in the slaying of Goliath symbolises the victory of the underdog in the face of overwhelming odds.</p> 

    <p class="lead text-body-secondary project-description">Although unveiled in 1504, David still retains a surprising and potent ability to cause controversy. In March 2023, the princpal of a Christian charter school in Florida was forced to resign after 6th grade children were shown images of the statue without seeking parental permission beforehand. Some parents regarded the statue as pornographic and would have refused to allow their children sight of it. Also in 2023, an Italian restaurant in Glasgow, Barolo, commissioned an advert for the Glasgow underground which pictured David from the knees up holding a slice of pizza with the slogan, "It doesn't get more Italian". The firm in charge of the advertising space on the underground refused the design, requiring David's genitalia to be covered.</p>

    <p class="lead text-body-secondary project-description">Now to more modern icons. In 1960s America arguably there was no greater icon than Marilyn Monroe. Glamourous, beautiful, troubled, Marylin fascinated a generation immersed in the golden age of Hollywood. Her fame was at its height when she died in August 1962. At that time Andy Warhol was experimenting with large silk screen portraits of famous actors. When Marilyn Monroe died, Warhol started making silk screens of her which led up to the series he created in 1964; 5 large (40 x 40 inch) portraits based on a publicity shot of Monroe taken for her 1953 film Niagra. The 5 silkscreen portraits each had distinct backgrounds; red, orange, light blue, sage blue and turquoise. While stacked in Warhol's studio, the Factory, Dorothy Podber, a performance artist, asked Warhol if she could shoot the screen prints. Warhol agreed believing Podber intended to photograph them. Instead she produced a small revolver and fired a shot into four of the paintings, adding yet another insult to the collective memory of Monroe. These 4 paintings became known as the Shot Marilyns.</p> 

    <p class="lead text-body-secondary project-description">In 2022, Shot Sage Blue Marilyn sold for $195 million in New York, the most expensive work of 20th century art ever sold at auction.</p>   

    <p class="lead text-body-secondary project-description">And so the idea emerged of David rendered as a modern icon through the medium of Warhol's Shot Marylins and presented as gender fluid with a translucent modesty leaf which, rather than concealing, attracts attention. Entitled "Not Shot David", this makes David fully visible with an air of flamboyance and celebration. Icons are held sacred because they represent something we all seek; they symbolise something essential in our humanity which is visible but unattainable. They teach us something - embodying virtues and desires such as beauty, strength, power, riches, fame ... all the baubles we become preoccupied with and which drive our aspiration. By rendering David in a way which is sexually ambiguous, a contemporary layer is added which firmly situates David in the present. These images hold that we should celebrate difference, that our sexuality is natural and inoffensive, and that we should not just tolerate diversity, but that we should expect and embrace it.</p> 

