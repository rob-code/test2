<div class="display-6 section-title">SHIPPING</div>

<div class="display-6 policy-title">Processing Times</div>

<p class="policy-text">We aim to dispatch orders within 5 working days of being placed. If due to unforseen events or print delays our processing times are longer, this will be communicated on the website.</p>

<div class="display-6 policy-title">UK Shipping</div>

<p class="policy-text">There is a charge for postage on each order of £5. Orders over £100 will be sent free of charge. We post Royal Mail signed for service. Please be prepared to sign for your order upon arrival or collect from your local Royal Mail depot. Royal Mail's delivery aim for 1st class is the next working day after posting, their delivery aim for 2nd class postage is 2-3 working days. We cannot be held responsible for delays caused by Royal Mail and once we have posted an order we cannot influence how quickly Royal Mail will deliver. If the item has not been delivered 10 working days after the due date then we can contact Royal Mail for an investigation and compensation.</p>


<div class="display-6 policy-title">International Shipping</div>

<p class="policy-text">The cost of shipping varies depending on the country you are in.</p>

<p class="policy-text">All international orders are posted using Royal Mail's International Tracked and Signed For service where available. Countries where this service is not available will use the next best option, usually International Tracked. Their estimated delivery aims are 5-7 working days but with checks in customs we find it's usually 2-3 weeks in total.</p>

