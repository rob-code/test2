<div class="display-6 policy-title">Customs and import taxes</div>

<p class="policy-text">Customs in the destination country have the right to hold items for checking and neither Royal Mail nor Robert Brice Art can influence the time it takes to clear customs in the destination country. We do add the relevant customs information to the parcels to ensure progress through customs is as speedy as possible.</p>

<p class="policy-text">The buyer is responsible for any customs duties, VAT, import taxes or courier handling fees that may apply on delivery. The prices you see on our website do not include these additional import fees. The charges will vary between destination countries, if you have any questions about what to expect please contact us before ordering.</p>

<p class="policy-text">If you wish to return an order please contact us first as you will need to provide specific customs information for easy return to the UK.</p>


<div class="display-6 section-title">RETURNS AND REFUNDS</div>

<div class="display-6 policy-title">Damaged or defective items</div>

<p class="policy-text">We only exchange items if they are defective or damaged upon arrival, and where items have not been handled other than for opening and inspection. This is especially important for giclée art prints which can be easily damaged if mishandled.</p>

<p class="policy-text">Please contact us within 14 days of delivery to let us know if your item has arrived damaged. If you have framed a giclée art print and subsequently report damage, we will be unable to consider the item for return or refund.</p>

<p class="policy-text">If reporting damage, you will need to provide clear photographic evidence of the damage, as well as photographic evidence of the condition of the packaging on arrival. Items must be sent back within 30 days of delivery. Depending on where you live, the time it may take for your exchanged product to reach you may vary.</p>

<div class="display-6 policy-title">Returns</div>

<p class="policy-text">Our goal is to delight you when you open a package from the Robert Brice Art. However if you're not happy with your purchase please contact us within 14 days of delivery and we can discuss a refund or an exchange of the item. If more than 14 days have gone by since your purchase, unfortunately we can’t offer you a refund or exchange.</p>

<p class="policy-text">To be eligible for a return, your item must be unused and in the same condition that you received it. Our prints must be handled carefully to ensure they do not have any dents, creases or scuff marks left by heavy handling or from poor packaging when they are posted back. Items must be sent back within 30 days of delivery.</p>

<p class="policy-text">If an item is returned with obvious signs of use or damage caused by you, the customer, we have the right to refuse a refund, or offer only a partial refund. For giclée art prints, we recommend that the prints are returned carefully rolled with the tissue paper they arrived in, and are rolled around a suitable tube or other item, rather than free-rolled by hand. This helps to avoid creases and dents to the paper during rolling. We also strongly recommend that you avoid touching the printed area of the prints as the surface of giclée art prints is extremely delicate.</p>

<div class="display-6 policy-title">Refunds</div>

<p class="policy-text">Once your return is received and inspected, we will notify you whether your request for a refund is approved or rejected.</p>

<p class="policy-text">If it's approved, then your refund will be processed, and a credit will automatically be applied to the original method of payment, within a certain amount of days. Items that are returned to us without prior notice will not be accepted. </p>

<div class="display-6 policy-title">Late or missing refunds</div>

<p class="policy-text">If you haven’t received a refund, first recheck your bank account. Then contact your credit card company or bank, it may take some time before your refund is officially processed. </p>

<p class="policy-text">If you’ve done all of this and you still have not received your refund yet, please contact us.</p>

<div class="display-6 policy-title">Return Shipping</div>

<p class="policy-text">Shipping costs are not covered by Robert Brice Art, unless in the case of a defective print. We recommend using a tracked or signed for shipping service or purchasing shipping insurance in case your return is lost or damaged in the post. We cannot guarantee that we will receive your returned item. If the postal service you choose loses or damages the item, as the sender it is your responsibility to claim back any monetary loss.</p>

